    public static LevelSettings clone(LevelSettings original)
    {
        LevelSettings clone = new LevelSettings();
        clone.sunPosition = new Vector3f(original.sunPosition);
        clone.sunPositionScale = original.sunPositionScale;
        clone.sunColor = new Vector4f(original.sunColor);
        clone.ambientColor = new Vector4f(original.ambientColor);
        clone.sunMultiplier = original.sunMultiplier;
        clone.exposure = original.exposure;
        clone.fogColor = new Vector4f(original.fogColor);
        clone.fogNear = original.fogNear;
        clone.fogFar = original.fogFar;
        clone.rimColor = new Vector4f(original.rimColor);
        clone.rimColor2 = new Vector4f(original.rimColor2);
        clone.bakedShadowAmount = original.bakedShadowAmount;
        clone.bakedShadowBlur = original.bakedShadowBlur;
        clone.bakedAOBias = original.bakedAOBias;
        clone.bakedAOScale = original.bakedAOScale;
        clone.dynamicAOAmount = original.dynamicAOAmount;
        clone.dofNear = original.dofNear;
        clone.dofFar = original.dofFar;
        clone.zEffectAmount = original.zEffectAmount;
        clone.zEffectBright = original.zEffectBright;
        clone.zEffectContrast = original.zEffectContrast;
        return clone;
    }

